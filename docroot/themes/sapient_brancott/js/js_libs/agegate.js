require([
  'templates/agegate',
  'templates/countries',
  'facebook-login',
  '../vendor/jquery.cookie/jquery.cookie',
  '../vendor/selectize/dist/js/selectize'], function(template, countriesTemplate, FacebookLogin) {

  var defaults = {
    facebookAppId: "",
    iframePath: "",
    gatewayUrl: "http://gateway.pernod-ricard-winemakers.com",
    defaultCountry: "UK",
    autoDetectCountry: true,
    keyMarkets: []
  }

  function AgeGate ( element, options ) {
    this.element = element
    this.settings = $.extend( {}, defaults, options )
    this._defaults = defaults

    this.init()
  }

  $.extend(AgeGate.prototype, {
    init: function () {
      this.element.append(template())

      this.$el = this.element.find('.js-age-gate')
      this.$dateControl = this.$el.find('.js-date-control')
      this.$fields = this.$el.find('.js-field')
      this.$button = this.$el.find('.js-enter')
      this.countryAge = 21
      this.today = new Date()
      this.blurTimeout = null
      this.countries = null
      this.gateway_status = 0

      this.settings.baseUrl = window.location.host + this.settings.iframePath

      this.$dateControl.addClass('date-control--year')

      this.$fields.on('keypress input', $.proxy(this.onFieldInput, this))
      this.$button.on('click', $.proxy(this.submitCheck, this))



      this.select = this.$el.find('.js-country-select')

      this.checkQuery()
      this.checkCookie()
      this.initFacebook()
      this.loadCountries($.proxy(function() {
        this.select = this.$el.find('.js-country-select').selectize()
        this.select.on('change', $.proxy(function() {
          var country = this.getCountryByCode(this.select.val())
          if (country) {
            this.showLegalMessage(country)
            this.updateLayout(country)
            this.setCountry(country)
          } else {
            this.countryAccess = this.countryCode = this.countryAge = ''
          }
        }, this))
      }, this))
    },

    initFacebook: function() {

      if (this.settings.facebookAppId === "") return this.$el.find('.js-facebook').hide()

      var facebookLogin = new FacebookLogin(this.settings.facebookAppId)

      facebookLogin.emitter.on('login', $.proxy(function() {
        this.$el.find('.js-login-facebook').removeClass('btn-facebook--loading')
        $.cookie('age_checked', 'facebook', { expires: 7, path: '/' })
        $.removeCookie('age_checked_failed')
        this.close()
      }, this))

      facebookLogin.emitter.on('fail', $.proxy(function() {
        this.$el.find('.js-login-facebook').removeClass('btn-facebook--loading')
      }, this))

      facebookLogin.init()

      this.$el.find('.js-login-facebook').on('click', function() {
        // Will connect to facebook
        $(this).addClass('btn-facebook--loading')
        facebookLogin.login()
      })
    },

    checkQuery: function () {
      var queries = [], hash;
      var hashes = window.location.href.slice(window.location.href.indexOf('?') + 1).split('&');
      for(var i = 0; i < hashes.length; i++)
      {
          hash = hashes[i].split('=');
          queries.push(hash[0]);
          queries[hash[0]] = hash[1];
      }
      if (queries.agegate && queries.agegate === 'false') {
        var fakeCookieValue = queries.agegate + ':1:1:1';
        $.cookie('age_checked', fakeCookieValue, { path: '/' });
        $.removeCookie('age_checked_failed', {path: '/'});
      }
    },

    checkCookie: function() {

      var age_checked = $.cookie('age_checked') ? 1 : 0
        , age_checked_failed = $.cookie('age_checked_failed') ? 1 : 0

      if (age_checked) return this.close()
      if (!age_checked && !age_checked_failed) {
        // Check global

        window.globalCookieCheck = $.proxy(function(cookie) {
          if (cookie !== "") {
            $.cookie('age_checked', cookie, { expires: 7, path: '/' });
            $.removeCookie('age_checked_failed', {path: '/'});
            this.close()
          }
          window.globalCookieCheck = null
        }, this)

        this.globalIframe = $('<iframe>')
          .appendTo(this.element)
          .attr('src', this.settings.gatewayUrl + '/agegate-cookie/uav-iframe?l=' + this.settings.baseUrl)
          .css('display', 'none')
      }
    },

    submitCheck: function(e) {
      e.preventDefault()

      if (!this.countryCode) { this.errorMessage(this.messages['no-country-selected']); return; }
      if (!this.countryAccess || !this.countryAge) { this.errorMessage(this.messages['country-not-allowed']); return; }
      if (!this.checkDate()) { this.errorMessage(this.messages['not-old-enough']); return; }
      //redirect to a locale
      this.saveAgeChecked(this.countryCode)
      this.close()
    },

    checkDate: function() {
      this.day = parseInt(this.$fields.filter('#day').val(), 10)
      this.month = parseInt(this.$fields.filter('#month').val(), 10)
      this.year = parseInt(this.$fields.filter('#year').val(), 10)

      if (isNaN(this.day) && this.countryCode !== 'US') this.day = 1
      if (isNaN(this.month) && this.countryCode !== 'US') this.month = 1

      if (this.year == undefined) return

      this.birthday = new Date(this.year, this.month-1, this.day)
      this.age = this.calculateAge(this.birthday)

      //console.log(this.age, this.countryAge)

      if (this.age >= this.countryAge) return true
    },

    saveAgeChecked: function() {
        var args=arguments,
            c_code= args[0].toLowerCase(),
            url = window.location.host,
            final_url = "";

            if(c_code !== 'nz' && c_code !== 'au' && c_code !== 'gb') {
                final_url="/en";
            } 
            else {
                final_url = "/en-"+c_code;
            }
        
       location.href=final_url;


      $.cookie('age_checked', this.makeAgeCheckCookieValue(), { path: '/' });
      $.removeCookie('age_checked_failed', {path: '/'});

      if ($('#remember-me').prop('checked')) {
        this.saveGlobal()
      }
    },

    makeAgeCheckCookieValue: function() {
      return this.countryCode+':'+this.year+':'+this.month+':'+this.day
    },

    saveGlobal: function() {
      this.globalIframe = $('<iframe>')
        .appendTo(this.element)
        .attr('src', this.settings.gatewayUrl + '/agegate-cookie/uav-iframe?l=' + this.settings.baseUrl + '&sc=true&data=' + this.makeAgeCheckCookieValue())
        .css('display', 'none')
    },

    calculateAge: function(birthday) { // birthday is a date
      var ageDifMs = (new Date).valueOf() - birthday.getTime();
      var ageDate = new Date(ageDifMs); // miliseconds from epoch
      return ageDate.getUTCFullYear() - 1970;
    },

    loadCountries: function(cb) {
      $.ajax({
        dataType: "jsonp",
        // jsonp: false
        crossDomain: true,
        cache: true,
        jsonpCallback: "jsonpCallback",
        url: this.settings.gatewayUrl + '/aggregator/ageverification/messages-countries'
      }).done($.proxy(function(data) {
        if (data) {

          this.countries = data.countries
          this.messages = data.messages
          this.gateway_status = data.gateway_status

          if (this.settings.keyMarkets.length > 0) {
            this.addKeyMarketsToTop()
          }

          this.select.append(countriesTemplate(data))
          this.select.trigger('update')

          var that = this

          var selectCountry = function(country) {
            that.select.find('option[value="' + country.code + '"]').prop('selected', true)
            that.select.trigger('update')
            that.setCountry(country || that.countries[0])
            that.$el.addClass('agegate--loaded')
            that.showLegalMessage(country || that.countries[0])
            that.updateLayout(country || that.countries[0])
            cb()
          }

          var country
          if (this.settings.defaultCountry !== '') {
            country = $.map(this.countries, $.proxy(function(c) {
              return c.code === this.settings.defaultCountry ? c : null
            }, this))[0]
          }

          if (this.settings.autoDetectCountry) {
            $.ajax({
              dataType: "jsonp",
              // jsonp: false
              crossDomain: true,
              cache: true,
              jsonpCallback: "callback",
              url: 'http://gateway.pernod-ricard-winemakers.com/geoip/myip'
            }).done($.proxy(function(json) {
              if (json.country_code !== '') {
                if (json.country_code === 'GB') json.country_code = 'UK'
                country = $.map(this.countries, $.proxy(function(c) {
                  return c.code === json.country_code ? c : null
                }, this))[0]
              }
              selectCountry(country)
            }, this))
          } else {
            selectCountry(country)
          }

        }
      }, this))
    },

    addKeyMarketsToTop: function() {
      for (var i = 0; i < this.settings.keyMarkets.length; i++) {
        // var index = this.countries.indexOf(this.settings.keyMarkets[i])
        country = $.map(this.countries, $.proxy(function(c) {
          return c.code === this.settings.keyMarkets[i] ? c : null
        }, this))[0]

        this.countries.splice($.inArray(country, this.countries), 1)
        this.countries.unshift(country)
      }
    },

    setCountry: function(country) {
      this.countryAccess = country.access
      this.countryCode = country.code
      this.countryAge = country.age
    },

    getCountryByCode: function(code) {
      if (this.countries) {
        for (var i = 0; i < this.countries.length; i++) {
          if (this.countries[i].code == code) {
            return this.countries[i];
          }
        }
      }
    },

    onFieldInput: function (e) {
      var $field = $(e.target)
        , val = $field.val()
        , valInt = parseInt(val, 10)
        , type = $field.attr('id')

      if (!isNaN(valInt) && valInt > 0) {
        if (type === 'year' && val.length == parseInt($field.attr('maxlength'))) {
          if (this.countryCode && this.countryCode === 'US') return
          if (this.isSameYear(valInt)) {
            this.$dateControl.removeClass('date-control--year').addClass('date-control--month')
            this.$fields.filter('#month').focus()
          }
        } else if (type === 'month') {
          if (this.countryCode && this.countryCode === 'US') return
          if (this.isSameMonth(valInt)) {
            this.$dateControl.removeClass('date-control--month')
            this.$fields.filter('#day').focus()
          }
        }
      }
    },

    errorMessage: function(message) {
      alert(message)
    },

    close: function() {
      this.$el.addClass('age-gate--closed')
      this.$el.trigger('closed')
      setTimeout($.proxy(function() {
        this.$el.remove()
      }, this), 1000)
    },

    validateYear: function(year) {
      return this.today.getFullYear() - year > this.countryAge
    },

    isSameYear: function(year) {
      return this.today.getFullYear() - year === this.countryAge
    },

    isSameMonth: function(month) {
      return this.today.getMonth()+1 === month
    },

    validateMonth: function(month) {
      return month < this.today.getMonth()+1 && month < 13
    },

    validateDay: function(day) {
      return day <= this.today.getDate() && day < 32
    },

    showLegalMessage: function(country) {
      if (this.$el.find('.js-uk-legal').length > 0) {
        if (country.code === 'UK') this.$el.find('.js-uk-legal').show()
        else this.$el.find('.js-uk-legal').hide()
      }
    },

    updateLayout: function(country) {
      if (country.code === 'US') {
        this.$dateControl.removeClass('date-control--year')
        this.$dateControl.removeClass('date-control--month')
      } else {
        this.$dateControl.addClass('date-control--year')
      }
    }
  });

  window.AgeGate = AgeGate
  window.ageGateReady && window.ageGateReady()
})
